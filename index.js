const r1_button = document.getElementById("r1_button");
const r1page = document.getElementById("r1page");
r1page.style.display = "none";
    r1_button.addEventListener("click",event => {
        if (r1page.style.display === "none") {
            r1page.style.display = "block";
        } 
        else {
            r1page.style.display = "none";
        }
    });

